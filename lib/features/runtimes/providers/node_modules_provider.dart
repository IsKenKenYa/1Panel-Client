import 'package:flutter/material.dart';
import 'package:onepanel_client/core/presentation/async_state_notifier.dart';
import 'package:onepanel_client/core/services/logger/logger_service.dart';
import 'package:onepanel_client/data/models/runtime_models.dart';
import 'package:onepanel_client/features/runtimes/models/runtime_manage_args.dart';
import 'package:onepanel_client/features/runtimes/services/node_runtime_service.dart';

class NodeModulesProvider extends ChangeNotifier with AsyncStateNotifier {
  NodeModulesProvider({
    NodeRuntimeService? service,
  }) : _service = service ?? NodeRuntimeService();

  final NodeRuntimeService _service;

  int? _runtimeId;
  String _runtimeName = '';
  String _packageManager = 'npm';
  String _keyword = '';
  String _moduleInput = '';
  List<NodeModuleInfo> _items = const <NodeModuleInfo>[];
  bool _isOperating = false;

  String get runtimeName => _runtimeName;
  String get packageManager => _packageManager;
  String get keyword => _keyword;
  String get moduleInput => _moduleInput;
  List<NodeModuleInfo> get items => _items;
  bool get isOperating => _isOperating;

  List<NodeModuleInfo> get filteredItems {
    final pattern = _keyword.trim().toLowerCase();
    if (pattern.isEmpty) {
      return _items;
    }
    return _items.where((item) {
      return item.name.toLowerCase().contains(pattern) ||
          item.description.toLowerCase().contains(pattern);
    }).toList(growable: false);
  }

  Future<void> initialize(RuntimeManageArgs args) async {
    _runtimeId = args.runtimeId;
    _runtimeName = args.runtimeName ?? '';
    _packageManager = (args.packageManager?.trim().isEmpty ?? true)
        ? 'npm'
        : args.packageManager!.trim();
    await load();
  }

  void updateKeyword(String value) {
    _keyword = value;
    notifyListeners();
  }

  void updateModuleInput(String value) {
    _moduleInput = value;
    notifyListeners();
  }

  void updatePackageManager(String value) {
    _packageManager = value;
    notifyListeners();
  }

  Future<void> load() async {
    final runtimeId = _runtimeId;
    if (runtimeId == null) {
      setError('runtime.detail.loadFailed');
      return;
    }
    setLoading();
    try {
      _items = await _service.loadModules(runtimeId);
      setSuccess(isEmpty: _items.isEmpty);
    } catch (error, stackTrace) {
      appLogger.eWithPackage(
        'features.runtimes.providers.node_modules',
        'load failed',
        error: error,
        stackTrace: stackTrace,
      );
      _items = const <NodeModuleInfo>[];
      setError('runtime.detail.loadFailed');
    }
  }

  Future<bool> installInputModule() async {
    final runtimeId = _runtimeId;
    final module = _moduleInput.trim();
    if (runtimeId == null || module.isEmpty) {
      return false;
    }
    _isOperating = true;
    clearError(notify: false);
    notifyListeners();
    try {
      await _service.installModule(
        runtimeId: runtimeId,
        module: module,
        packageManager: _packageManager,
      );
      _moduleInput = '';
      await load();
      return true;
    } catch (error, stackTrace) {
      appLogger.eWithPackage(
        'features.runtimes.providers.node_modules',
        'install failed',
        error: error,
        stackTrace: stackTrace,
      );
      setError('runtime.detail.operateFailed', notify: false);
      return false;
    } finally {
      _isOperating = false;
      notifyListeners();
    }
  }

  Future<bool> uninstallModule(String module) async {
    final runtimeId = _runtimeId;
    if (runtimeId == null || module.trim().isEmpty) {
      return false;
    }
    _isOperating = true;
    clearError(notify: false);
    notifyListeners();
    try {
      await _service.uninstallModule(
        runtimeId: runtimeId,
        module: module.trim(),
        packageManager: _packageManager,
      );
      await load();
      return true;
    } catch (error, stackTrace) {
      appLogger.eWithPackage(
        'features.runtimes.providers.node_modules',
        'uninstall failed',
        error: error,
        stackTrace: stackTrace,
      );
      setError('runtime.detail.operateFailed', notify: false);
      return false;
    } finally {
      _isOperating = false;
      notifyListeners();
    }
  }
}
