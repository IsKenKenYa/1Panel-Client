import 'package:flutter/material.dart';
import 'package:onepanel_client/core/i18n/l10n_x.dart';
import 'package:onepanel_client/features/runtimes/models/runtime_manage_args.dart';
import 'package:onepanel_client/features/runtimes/providers/node_modules_provider.dart';
import 'package:onepanel_client/features/runtimes/utils/runtime_l10n_helper.dart';
import 'package:onepanel_client/features/shell/controllers/current_server_controller.dart';
import 'package:onepanel_client/features/shell/widgets/server_aware_page_scaffold.dart';
import 'package:onepanel_client/shared/widgets/operations/async_state_page_body_widget.dart';
import 'package:provider/provider.dart';

class NodeModulesPage extends StatefulWidget {
  const NodeModulesPage({
    super.key,
    required this.args,
  });

  final RuntimeManageArgs args;

  @override
  State<NodeModulesPage> createState() => _NodeModulesPageState();
}

class _NodeModulesPageState extends State<NodeModulesPage> {
  late final TextEditingController _searchController;
  late final TextEditingController _moduleController;

  @override
  void initState() {
    super.initState();
    _searchController = TextEditingController();
    _moduleController = TextEditingController();
    WidgetsBinding.instance.addPostFrameCallback((_) {
      if (!mounted || !context.read<CurrentServerController>().hasServer) {
        return;
      }
      context.read<NodeModulesProvider>().initialize(widget.args);
    });
  }

  @override
  void dispose() {
    _searchController.dispose();
    _moduleController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    final l10n = context.l10n;
    return Consumer<NodeModulesProvider>(
      builder: (context, provider, _) {
        if (_moduleController.text != provider.moduleInput) {
          _moduleController.text = provider.moduleInput;
        }
        final title = provider.runtimeName.trim().isEmpty
            ? l10n.operationsNodeModulesTitle
            : '${provider.runtimeName} · ${l10n.operationsNodeModulesTitle}';
        return ServerAwarePageScaffold(
          title: title,
          onServerChanged: () =>
              context.read<NodeModulesProvider>().initialize(widget.args),
          actions: <Widget>[
            IconButton(
              onPressed: provider.isLoading ? null : provider.load,
              icon: const Icon(Icons.refresh),
              tooltip: l10n.commonRefresh,
            ),
          ],
          body: Column(
            children: <Widget>[
              Padding(
                padding: const EdgeInsets.fromLTRB(16, 16, 16, 8),
                child: Column(
                  children: <Widget>[
                    TextField(
                      controller: _searchController,
                      onChanged: provider.updateKeyword,
                      decoration: InputDecoration(
                        hintText: l10n.commonSearch,
                        prefixIcon: const Icon(Icons.search),
                      ),
                    ),
                    const SizedBox(height: 8),
                    Row(
                      children: <Widget>[
                        Expanded(
                          child: TextField(
                            controller: _moduleController,
                            onChanged: provider.updateModuleInput,
                            decoration: InputDecoration(
                              labelText: l10n.commonName,
                            ),
                          ),
                        ),
                        const SizedBox(width: 8),
                        DropdownButton<String>(
                          value: provider.packageManager,
                          items: const <String>['npm', 'yarn', 'pnpm']
                              .map(
                                (item) => DropdownMenuItem<String>(
                                  value: item,
                                  child: Text(item),
                                ),
                              )
                              .toList(growable: false),
                          onChanged: provider.isOperating
                              ? null
                              : (value) {
                                  if (value != null) {
                                    provider.updatePackageManager(value);
                                  }
                                },
                        ),
                        const SizedBox(width: 8),
                        FilledButton(
                          onPressed:
                              provider.isOperating ? null : _installModule,
                          child: Text(l10n.appStoreInstall),
                        ),
                      ],
                    ),
                  ],
                ),
              ),
              Expanded(
                child: AsyncStatePageBodyWidget(
                  isLoading: provider.isLoading,
                  isEmpty: provider.isEmpty,
                  errorMessage:
                      localizeRuntimeError(l10n, provider.errorMessage),
                  onRetry: provider.load,
                  emptyTitle: l10n.runtimeEmptyTitle,
                  emptyDescription: l10n.runtimeEmptyDescription,
                  child: ListView.separated(
                    padding: const EdgeInsets.fromLTRB(16, 0, 16, 16),
                    itemCount: provider.filteredItems.length,
                    separatorBuilder: (_, __) => const SizedBox(height: 10),
                    itemBuilder: (context, index) {
                      final item = provider.filteredItems[index];
                      return Card(
                        child: ListTile(
                          title: Text(item.name),
                          subtitle: Text(
                            [
                              if (item.version.isNotEmpty) item.version,
                              if (item.description.isNotEmpty) item.description,
                            ].join('\n'),
                          ),
                          isThreeLine: item.description.isNotEmpty,
                          trailing: FilledButton.tonal(
                            onPressed: provider.isOperating
                                ? null
                                : () => _uninstallModule(item.name),
                            child: Text(l10n.appActionUninstall),
                          ),
                        ),
                      );
                    },
                  ),
                ),
              ),
            ],
          ),
        );
      },
    );
  }

  Future<void> _installModule() async {
    final success =
        await context.read<NodeModulesProvider>().installInputModule();
    if (!mounted) {
      return;
    }
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: Text(success
            ? context.l10n.commonSaveSuccess
            : context.l10n.commonSaveFailed),
      ),
    );
  }

  Future<void> _uninstallModule(String module) async {
    final success =
        await context.read<NodeModulesProvider>().uninstallModule(module);
    if (!mounted) {
      return;
    }
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: Text(success
            ? context.l10n.commonSaveSuccess
            : context.l10n.commonSaveFailed),
      ),
    );
  }
}
