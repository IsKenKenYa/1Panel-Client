import 'package:flutter_test/flutter_test.dart';
import 'package:mocktail/mocktail.dart';
import 'package:onepanel_client/data/models/runtime_models.dart';
import 'package:onepanel_client/features/runtimes/models/runtime_manage_args.dart';
import 'package:onepanel_client/features/runtimes/providers/node_modules_provider.dart';
import 'package:onepanel_client/features/runtimes/services/node_runtime_service.dart';

class _MockNodeRuntimeService extends Mock implements NodeRuntimeService {}

void main() {
  late _MockNodeRuntimeService service;
  late NodeModulesProvider provider;

  setUp(() {
    service = _MockNodeRuntimeService();
    when(() => service.loadModules(any())).thenAnswer(
      (_) async => const <NodeModuleInfo>[
        NodeModuleInfo(name: 'pm2', version: '5.0.0'),
      ],
    );
    when(
      () => service.installModule(
        runtimeId: any(named: 'runtimeId'),
        module: any(named: 'module'),
        packageManager: any(named: 'packageManager'),
      ),
    ).thenAnswer((_) async {});
    when(
      () => service.uninstallModule(
        runtimeId: any(named: 'runtimeId'),
        module: any(named: 'module'),
        packageManager: any(named: 'packageManager'),
      ),
    ).thenAnswer((_) async {});

    provider = NodeModulesProvider(service: service);
  });

  test('initialize loads node modules', () async {
    await provider.initialize(const RuntimeManageArgs(runtimeId: 5));

    expect(provider.items, hasLength(1));
    expect(provider.items.first.name, 'pm2');
  });

  test('installInputModule calls service', () async {
    await provider.initialize(const RuntimeManageArgs(runtimeId: 5));
    provider.updateModuleInput('eslint');

    final result = await provider.installInputModule();

    expect(result, isTrue);
    verify(
      () => service.installModule(
        runtimeId: 5,
        module: 'eslint',
        packageManager: 'npm',
      ),
    ).called(1);
  });
}
